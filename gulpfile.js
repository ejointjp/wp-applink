/////////////////////////////////////////////////////////////////////////////////////

// requirement

/////////////////////////////////////////////////////////////////////////////////////

var gulp = require('gulp');
var del = require('del');
var path = require('path');

/////////////////////////////////////////////////////////////////////////////////////

// config

/////////////////////////////////////////////////////////////////////////////////////

var config = require('./_config');
var dir = config.dir;
var file = config.file;

/////////////////////////////////////////////////////////////////////////////////////

// tasks

/////////////////////////////////////////////////////////////////////////////////////

// clean assets file
gulp.task('clean', function(){

  var deleteFiles = path.join(dir.rel.dist, dir.assets, dir.all, file.all);

  console.log('Deleted files: ' + deleteFiles);
  del(deleteFiles);
});

gulp.task('build', function(){
  var src = path.join(dir.rel.src, dir.sass, 'wp-applink.scss');
  var dist = path.join(dir.rel.dist, dir.assets, dir.sass)

  gulp.src(src)
    .pipe(gulp.dest(dist));
});
